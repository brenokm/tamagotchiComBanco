package Classes;


public class Tamago {
    
    private int id_tamago;
    private String nome_tamago;
    private int level_tamago;
    private int idade_tamago;
    private int energia_tamago;
    private int saude_tamago;
    private int fome_tamago;
    private int estadoM_tamago;
    private int higiene_tamago;
    private int sono_tamago;

    public Tamago() {
    }

    public Tamago(int id_tamago, String nome_tamago, int level_tamago, int idade_tamago, int energia_tamago, int saude_tamago, int fome_tamago, int estadoM_tamago, int higiene_tamago, int sono_tamago) {
        this.id_tamago = id_tamago;
        this.nome_tamago = nome_tamago;
        this.level_tamago = level_tamago;
        this.idade_tamago = idade_tamago;
        this.energia_tamago = energia_tamago;
        this.saude_tamago = saude_tamago;
        this.fome_tamago = fome_tamago;
        this.estadoM_tamago = estadoM_tamago;
        this.higiene_tamago = higiene_tamago;
        this.sono_tamago = sono_tamago;
    }

    
    
    
    public int getId_tamago() {
        return id_tamago;
    }

    public void setId_tamago(int id_tamago) {
        this.id_tamago = id_tamago;
    }

    public String getNome_tamago() {
        return nome_tamago;
    }

    public void setNome_tamago(String nome_tamago) {
        this.nome_tamago = nome_tamago;
    }

    public int getLevel_tamago() {
        return level_tamago;
    }

    public void setLevel_tamago(int level_tamago) {
        this.level_tamago = level_tamago;
    }

    public int getIdade_tamago() {
        return idade_tamago;
    }

    public void setIdade_tamago(int idade_tamago) {
        this.idade_tamago = idade_tamago;
    }

    public int getEnergia_tamago() {
        return energia_tamago;
    }

    public void setEnergia_tamago(int energia_tamago) {
        this.energia_tamago = energia_tamago;
    }

    public int getSaude_tamago() {
        return saude_tamago;
    }

    public void setSaude_tamago(int saude_tamago) {
        this.saude_tamago = saude_tamago;
    }

    public int getFome_tamago() {
        return fome_tamago;
    }

    public void setFome_tamago(int fome_tamago) {
        this.fome_tamago = fome_tamago;
    }

    public int getEstadoM_tamago() {
        return estadoM_tamago;
    }

    public void setEstadoM_tamago(int estadoM_tamago) {
        this.estadoM_tamago = estadoM_tamago;
    }

    public int getHigiene_tamago() {
        return higiene_tamago;
    }

    public void setHigiene_tamago(int higiene_tamago) {
        this.higiene_tamago = higiene_tamago;
    }

    public int getSono_tamago() {
        return sono_tamago;
    }

    public void setSono_tamago(int sono_tamago) {
        this.sono_tamago = sono_tamago;
    }
    
    
}
